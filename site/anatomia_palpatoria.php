<b>NOMENCLATURA:</b> ANATOMIA CL&Iacute;NICA FUNCIONAL<br>
<br>
<b>CARGA HORÁRIA:</b> 16H        
<br>
<br>
<b>EMENTA:</b>
Identificação tátil das estruturas anatômicas palpáveis do sistema
Musculo-esqueletico, sistema circulatório e do sistema nervoso.
<br>
<br>
<b>OBJETIVO:</b>
Estabelecer correlação entre o conhecimento teórico e pratico
Previamente adquirido nas disciplinas de conteúdo morfológico, com o ser vivo orientado no tempo e espaço.
<br><br>Desenvolver a acuidade tátil no futuro profissional da área da saúde
Capacitando-o a diferençar as estruturas e os componentes dos diferentes sistemas anatomo-morfo-fisiologicos.     
<br><br>Correlacionar as estruturas palpáveis com os diferentes critérios
Semiológicos pertinentes à ciência da fisioterapia.
