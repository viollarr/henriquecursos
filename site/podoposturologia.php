<h2>Curso de Formação em Podoposturologia</h2>

<h4>REPROGRAMAÇÃO POSTURAL ATRAVÉS DE PALMILHAS POSTURAIS COM ÊNFASE NA CONFECÇÃO DE PALMILHAS</h4>

<hr>


<h3>Introdução</h3>

<p>
A reeducação postural através de palmilhas posturais (podoposturologia) é uma técnica que busca integrar o conhecimento entre a influência dos pés, a postura e o equilíbrio ortostático.
</p>

<h3>Processo de avaliação </h3>

<p>
O processo se inicia com uma avaliação postural. Nela se observa o equilíbrio do tônus entre as cadeias musculares. São avaliadas as variáveis posturais estáticas e dinâmicas relacionadas com a cintura escapular e pélvica. Estes parâmetros podem estar simétricos ou assimétricos. O objetivo do tratamento é utilizar as palmilhas posturais para promover a simetria entre as tensões musculares. 
</p>

<h3>Processo de prescrição de palmilhas posturais</h3>

<p>
As palmilhas posturais agem imediatamente em alguns parâmetros posturais. Este tipo de tratamento necessita de acompanhamento constante. O atendimento é individual.  A avaliação postural clínica é imprescindível na prescrição de palmilhas. O que diferencia as palmilhas posturais das ortopédicas é o fato de que as palmilhas posturais atuam na postura equilibrando a tensão entre as cadeias de músculos que envolvem o esqueleto ósseo e, a palmilha ortopédica é destinada para os problemas nos pés. As palmilhas posturais extrapolam os problemas dos pés. Muitas vezes são prescritas para pacientes com pés normais e sem queixas clínicas podais. 
As peças podais fixadas em uma órtese denominada de palmilha postural têm como objetivo diminuir os desequilíbrios tônicos, buscar as simetrias e reduzir as disfunções nas informações que perturbam a postura e o equilíbrio. As informações necessárias para a regulação da postura estática e dinâmica também são decorrentes da planta dos pés. O tônus dos músculos do corpo reage aos estímulos que são efetuados na região plantar. 
</p>

<h3>Tratamento com palmilhas posturais é complementar</h3>

<p>
O tratamento com a podoposturologia não é feito isoladamente. Outras técnicas estão associadas. Quem indica e fabrica palmilhas, de maneira geral, tem pouco conhecimento sobre a complexidade do controle postural, desconhece a relação existente entre a visão, os receptores plantares e a disfunção do aparelho oclusal na repercussão postural. Este fato pode propiciar alterações lesivas na postura do indivíduo e gradativamente pode desenvolver o desconforto, dor e degeneração articular. 
</p>

<h3>Indicação de palmilhas posturais</h3>

<p>
O uso é muito comum nos problemas relacionados com os pés. Como em casos de metatarsalgias, fascites plantares, esporão de calcâneo, neuroma de Morton, pé cavo e plano, calcâneo valgo (pé pronado) e varo (pé supinado) e nas diferenças do comprimento dos membros inferiores onde ocorre a báscula da cintura pélvica. A indicação do tratamento poda postural se concentra nos transtornos funcionais. Visam corrigir as básculas e rotações da cintura escapular e pélvica. 
</p>

<h3>Tecnologias de avaliação complementar</h3>


<ol>
<li>
  <h3>Exame de baropodometria</h3>
  Outro aspecto que auxilia na avaliação da postura é a baropodometria. Sendo um exame objetivo e quantitativo analisa a pressão plantar sobre uma plataforma composta de sensores. Este exame visa mensurar e comparar as pressões desenvolvidas nos diferentes pontos da região plantar tanto na posição em pé ou na marcha. No pré e pós tratamento com palmilhas.
</li>
<li>
  <h3>Exame de estabilometria</h3>
  Da mesma forma a estabilometria auxilia na avaliação do equilíbrio. Mensura a projeção das forças do centro de gravidade no polígono de sustentação. Esta condição significa mensurar uma propriedade do corpo de se manter estável na postura ortostática. Esta estabilidade é uma dinâmica da tensão muscular em direção ao equilíbrio, o que de certa forma reduz o indivíduo a um ponto que pode ser registrado em gráficos.  Os resultados provenientes da estabilometria partem do pressuposto de que, quanto mais próximo o indivíduo estiver da linha base das abscissas e das coordenadas descritas como O (zero) mm, a postura se apresenta mais estável. Por outro lado, quanto mais distante o gráfico de registro postural estiver destas linhas de base, maior será a tendência de instabilidade corporal. E como conseqüência, maior a tensão muscular, maior gasto de energia, fadiga e compressão articular podendo colaborar com a degeneração articular (artrose).
</li>
</ol>
