      <p align="center"><strong>Ergonomia de Projeto e de Corre&ccedil;&atilde;o</strong></p>
      <p><strong>Ementa:</strong> Conceitos de Ergonomia, Fisioterapia e Terapias Manuais, aspectos legais e normativos em sa&uacute;de do trabalhador, situa&ccedil;&atilde;o das LER / DORT nos dias de hoje, formas de preven&ccedil;&atilde;o e tratamento, campo de atua&ccedil;&atilde;o do Fisioterapeuta nas empresas, negocia&ccedil;&atilde;o e relacionamento com clientes.<br /><br />
        <strong>Objetivo: </strong>Capacitar Fisioterapeutas na preven&ccedil;&atilde;o e tratamento das LER / DORT, desenvolvimento de programas preventivos para empresas, negocia&ccedil;&atilde;o com clientes e elabora&ccedil;&atilde;o de projetos e propostas.<br>
        <br />
        <b>CONTEÚDO PROGRAMÁTICO:</b><br> 
  <br>
      </p>      
        <strong>1 - A Sa&uacute;de dos Trabalhadores&nbsp;&nbsp;&nbsp;&nbsp; </strong><br />
      <ul>
        <li>Breve hist&oacute;rico</li>
        <li>A Sa&uacute;de dos Trabalhadores nos dias de hoje</li>
        <li>LER ou DORT, que nomenclatura adotar?</li>
        <li>LER/DORT: Situa&ccedil;&atilde;o atual e o impacto para as empresas</li><br />
      </ul>
        <strong>2 - Introdu&ccedil;&atilde;o &agrave; Ergonomia</strong>
      <ul>
        <li>Defini&ccedil;&otilde;es</li>
        <li>Aplica&ccedil;&otilde;es</li>
        <li>Dom&iacute;nios de Especializa&ccedil;&atilde;o em Ergonomia (F&iacute;sica, Cognitiva e Organizacional)</li>
        <li>Vis&atilde;o Ergon&ocirc;mica do Iceberg</li>
        <li>Os 10 tipos de solu&ccedil;&otilde;es em Ergonomia</li>
        <li>O Censo de Ergonomia (ferramenta de controle epidemiol&oacute;gico)</li><br />
      </ul>
        <strong>3 - Legisla&ccedil;&atilde;o em Seguran&ccedil;a e Sa&uacute;de Ocupacional</strong>
      <ul>
        <li>Normas Regulamentadoras - NRs (PPRA, PCMSO, CIPA, EPI, etc.)</li>
        <li>NR 17 - Ergonomia</li>
        <li>Anexos 1 e 2 da NR 17</li>
        <li>Nexo T&eacute;cnico Epidemiol&oacute;gico Previdenci&aacute;rio - NTEP</li>
      </ul>
        <strong>4 - Campo de Atua&ccedil;&atilde;o do Fisioterapeuta nas Empresas</strong>
      <ul>
        <li>Conhecimentos m&iacute;nimos necess&aacute;rios (Administra&ccedil;&atilde;o de Empresas, Sistemas de Gest&atilde;o, Seguran&ccedil;a e Medicina do Trabalho)</li>
        <li>Negocia&ccedil;&atilde;o com clientes e elabora&ccedil;&atilde;o de projetos e propostas</li>
        <li>Abordagem preventiva ou terap&ecirc;utica:</li><br />
     	- Programas preventivos (Ergonomia, Qualidade de Vida, Preven&ccedil;&atilde;o de LER/DORT, etc.)
        - Terceiriza&ccedil;&atilde;o de ambulat&oacute;rios nas empresas<br />
        - Gin&aacute;stica Laboral (Fisioterapia X Educa&ccedil;&atilde;o F&iacute;sica)<br />
        <li>Rela&ccedil;&atilde;o custo/benef&iacute;cio das interven&ccedil;&otilde;es ergon&ocirc;micas</li>
        <li>Principais argumentos para convencer o cliente a investir em preven&ccedil;&atilde;o</li><br />
       </ul>
        <strong>5 - Fisiologia Aplicada ao Trabalho</strong>
       <ul>
        <li>Trabalho muscular est&aacute;tico X din&acirc;mico</li>
        <li>A Fadiga e os Mecanismos de Regula&ccedil;&atilde;o</li>
        <li>Estresse ocupacional</li>
        <li>T&eacute;dio e Monotonia</li>
        <li>Hor&aacute;rios de trabalho e h&aacute;bitos alimentares</li>
        <li>Trabalho em turnos</li><br />
      </ul>
        <strong>6 - Biomec&acirc;nica e Antropometria</strong>
	  <ul>
        <li>Posturas do corpo para o trabalho</li>
        <li>Diferen&ccedil;as individuais</li>
        <li>Antropometria est&aacute;tica</li>
        <li>Antropometria din&acirc;mica e funcional</li>
        <li>Principais situa&ccedil;&otilde;es de sobrecarga biomec&acirc;nica no trabalho</li>
        <li>Recomenda&ccedil;&otilde;es Ergon&ocirc;micas para utiliza&ccedil;&atilde;o correta do sistema locomotor</li><br />
      </ul>
        <strong>7 - Planejamento de Postos de Trabalho</strong>
      <ul>
        <li>Caracter&iacute;sticas b&aacute;sicas do posto de trabalho visando sua adequa&ccedil;&atilde;o &agrave;s medidas dos seres humanos</li>
        <li>Tabelas antropom&eacute;tricas, quando e como us&aacute;-las</li>
        <li>Padr&otilde;es antropom&eacute;tricos mais comuns (ind&uacute;strias e escrit&oacute;rios)</li><br />
      </ul>
        <strong>8 - Principais Dist&uacute;rbios Osteomusculares Relacionados ao Trabalho</strong>
      <ul>
        <li>Causas mais freq&uuml;entes</li>
        <li>Tratamento</li><br />
      </ul>
        <strong>9 - Preven&ccedil;&atilde;o dos Dist&uacute;rbios Relacionados ao Uso de Computadores</strong>
      <ul>
        <li>Principais situa&ccedil;&otilde;es antiergon&ocirc;micas no trabalho com computador</li>
        <li>Recomenda&ccedil;&otilde;es para trabalhar corretamente com computador</li>
        <li>Par&acirc;metros a serem observados numa interven&ccedil;&atilde;o ergon&ocirc;mica em escrit&oacute;rios</li>
        <li>Recomenda&ccedil;&otilde;es b&aacute;sicas para escolha do mobili&aacute;rio e das cadeiras</li>
        <li>Especifica&ccedil;&atilde;o de acess&oacute;rios ergon&ocirc;micos</li><br />
       </ul>
        <strong>10 - Fatores Ambientais</strong>
       <ul>
        <li>Ilumina&ccedil;&atilde;o e o conforto visual</li>
        <li>Conforto t&eacute;rmico no trabalho intelectual</li>
        <li>Conforto ac&uacute;stico no trabalho intelectual</li><br />
      </ul>
        <strong>11 - Preven&ccedil;&atilde;o de Dist&uacute;rbios na Coluna Vertebral</strong>
      <ul>
        <li>Os 10 princ&iacute;pios ergon&ocirc;micos visando a preven&ccedil;&atilde;o dos dist&uacute;rbios lombares</li>
        <li>Principais situa&ccedil;&otilde;es de sobrecarga</li>
        <li>Movimenta&ccedil;&atilde;o individual de cargas</li>
        <li>Cuidados na sele&ccedil;&atilde;o dos trabalhadores para movimenta&ccedil;&atilde;o de cargas</li><br />
      </ul>
        <strong>12 - Preven&ccedil;&atilde;o de Dist&uacute;rbios nos Membros Superiores</strong>
	  <ul>
        <li>Principais caracter&iacute;sticas dos membros superiores</li>
        <li>Principais situa&ccedil;&otilde;es de sobrecarga</li>
        <li>Os 15 princ&iacute;pios ergon&ocirc;micos visando a preven&ccedil;&atilde;o de dist&uacute;rbios em membros superiores</li>
        <li>Medidas m&eacute;dicas e administrativas complementares</li><br />
      </ul>
        <strong>13 - Organiza&ccedil;&atilde;o do Trabalho</strong>
      <ul>
        <li>O que &eacute; Organiza&ccedil;&atilde;o do Trabalho?</li>
        <li>Caracter&iacute;sticas ideais da Organiza&ccedil;&atilde;o do Trabalho visando a preven&ccedil;&atilde;o de transtornos ao trabalhador</li>
        <li>Fatores causadores de sobrecarga</li>
        <li>Organiza&ccedil;&atilde;o do Trabalho segundo a NR 17 &ndash; Ergonomia (Norma Regulamentadora do Minist&eacute;rio do Trabalho e Emprego)</li>
        <li>Como detectar os fatores de organiza&ccedil;&atilde;o do trabalho</li>
        <li>Formas de preven&ccedil;&atilde;o</li>
        <li>A import&acirc;ncia dos mecanismos de regula&ccedil;&atilde;o</li>
        <li>Ergonomia no m&eacute;todo de trabalho</li><br />
      </ul>
        <strong>14 - An&aacute;lise Ergon&ocirc;mica do Trabalho (AET)</strong>
      <ul>
        <li>Os 3 princ&iacute;pios b&aacute;sicos a serem considerados em uma An&aacute;lise Ergon&ocirc;mica</li>
        <li>Os 5 fundamentos da Interven&ccedil;&atilde;o Ergon&ocirc;mica</li>
        <li>Formul&aacute;rio modelo para a AET (An&aacute;lise Ergon&ocirc;mica do Trabalho)</li>
        <li>M&eacute;todos complementares (LPR-NIOSH, REBA, RULA, Moore e Garg, Checklists, dentre outros) - teoria e pr&aacute;tica</li>
