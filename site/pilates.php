Preparar o acadêmico de Fisioterapia para um conjunto de decisões e para uma ação manual objetiva e eficaz com o Pilates.
Capacitar o acadêmico de Fisioterapia a prescrever, contra indicar e dominar a execução e aplicação do Pilates.
